    //a method that searches for the product belonging to a category when search button is pressed.
    private void btnSearchCategoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchCategoryActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblProductInfo.getModel();
        
        String search_category = (String) cmbSearchCategory.getSelectedItem();
        ArrayList<POJO_Class> category_list = new ArrayList<>();
        String result="";
        
        if(model.getRowCount()<=0){
            JOptionPane.showMessageDialog(rootPane,"Please make sure that there is at least some products added.","Search Error!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        else{
            for(POJO_Class list_search:list){
                if(list_search.getCategory().equals(search_category)){
                    category_list.add(list_search);
                    result+=list_search.getName()+"\n";
                }
            }
            JOptionPane.showMessageDialog(rootPane,"There seems to be "+category_list.size()+" item belonging to "+search_category+" category."+"\n"+result,"Search Result",JOptionPane.INFORMATION_MESSAGE);   
        }
    }//GEN-LAST:event_btnSearchCategoryActionPerformed

