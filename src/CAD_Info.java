    //a method that adds product when triggered
    private void btnAddProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddProductActionPerformed
        //putting the variables and actions to avoid the exception problems
        try{
            String product_id = fldId.getText().trim();
            String product_category = (String)cmbCategory.getSelectedItem();
            String product_name = fldName.getText();
            int product_price = Integer.parseInt(fldPrice.getText());
            
            if(product_price<5000){
                JOptionPane.showMessageDialog(rootPane,"Minimum price of the product should be 5000","Error!",JOptionPane.ERROR_MESSAGE);
                fldPrice.setText(null);
                return;
            }
            
            String product_recommendation = "None";
            if(chkOrganization.isSelected()){
                product_recommendation = "Organization";
            }
            else if(chkPerson.isSelected()){
                product_recommendation = "Person";
            }

            String product_range;
            if(product_price>4999 && product_price<=30000){
                product_range = "Low";
                radioLow.setSelected(true);
            }
            else if(product_price>30000 && product_price<=70000){
                product_range = "Mid";
                radioMid.setSelected(true);
            }
            else{
                product_range = "High";
                radioHigh.setSelected(true);
            }
            
            if(product_id.isEmpty()||product_category.isEmpty()||product_name.isEmpty()||fldPrice.getText().isEmpty()||product_recommendation.isEmpty()||product_range.isEmpty()){
                JOptionPane.showMessageDialog(rootPane,"Please be sure to enter all the required values to further process.","Error!!",JOptionPane.ERROR_MESSAGE);
            }
            else{
                boolean rowEmpty = false;
                if(tblProductInfo.getRowCount()==0){
                    rowEmpty = true;
                }
                else if(tblProductInfo.getRowCount()>0){
                    for(POJO_Class check: list){
                        if(check.getId().equals(product_id)){
                            rowEmpty = false;
                            JOptionPane.showMessageDialog(rootPane,"This product id already exists.","Error!",JOptionPane.ERROR_MESSAGE);
                            break;
                        }
                        else{
                            rowEmpty = true;
                        }
                    }
                }
                if(rowEmpty){
                    DefaultTableModel model = (DefaultTableModel) tblProductInfo.getModel();
                    model.addRow( new Object[]{product_id,product_category,product_name,product_price,product_recommendation,product_range});
                    POJO_Class object = new POJO_Class(product_id,product_category,product_name,product_price,product_recommendation,product_range);
                    list.add(object);
                }
            }
            
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(rootPane,"Please enter a number.","Error!",JOptionPane.ERROR_MESSAGE);
            fldPrice.setText(null);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(rootPane,"Please make sure to complete the required information.","Invalid!!!",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddProductActionPerformed

