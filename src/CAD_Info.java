    //a method that searches the products as per the price provided when search button is pressed.
    private void btnSearchPriceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchPriceActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblProductInfo.getModel();
        
        int searchPrice =  Integer.parseInt(fldSearchPrice.getText());
        if(model.getRowCount()==0){
            JOptionPane.showMessageDialog(rootPane,"Please add some products first.","Invalid!!!",JOptionPane.ERROR_MESSAGE);
        }
        else{
            SelectionSort.sort(list);
            model.setRowCount(0);
            for(POJO_Class overWrite:list){
                model.addRow(new Object[]{overWrite.getId(),overWrite.getCategory(),overWrite.getName(),overWrite.getPrice(),overWrite.getRecommendation(),overWrite.getRange()});
            }

            int index = binarySearchPrice(list, 0, list.size()-1, searchPrice);
            if(index == -1){
                JOptionPane.showMessageDialog(null,"Search price:- "+searchPrice+ " not found. Please insert correct price.","Invalid Price!",JOptionPane.ERROR_MESSAGE);
            } 
            else{
                POJO_Class data =  list.get(index);
                for(int i=0;i<1;i++){
                    JOptionPane.showMessageDialog(rootPane,"Product ID:- "+ data.getId()+
                            "\n Product Category:- "+ data.getCategory()+
                            "\n Product Name:- "+ data.getName()+
                            "\n Product Price:- "+ data.getPrice()+
                            "\n Product Recommendation:- "+ data.getRecommendation()+
                            "\n Product Range:- "+ data.getRange()
                            ,"Search Result", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btnSearchPriceActionPerformed

