    //a method to find the location of the provided price for searching 
    public static int binarySearchPrice(ArrayList<POJO_Class> search, int low, int high, int value){
        if(low<=high){
            int mid = (low+high)/2;
            if(search.get(mid).getPrice()==value){
                return mid;
            } 
            else if(search.get(mid).getPrice()>value){
                return binarySearchPrice(search,low,mid-1,value);
            }
            else{
                return binarySearchPrice(search,mid+1,high, value);
            }
        }
        else{
            return -1;
        }
    }
    
